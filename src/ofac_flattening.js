async function listDatabases(client) {
  let query = {
    "_source.referents": {
      $in: ofacIds,
    },
  };

  let result = await client
    .db("test_db")
    .collection("yente-entities_master")
    .find(query)
    .toArray();
  let jsonResponse = result.map((item) => {
    console.log(item._source.names.length);
    let actualNameObj = {
      open_sanction_id: item._id,
      name: item._source.caption,
      alias: item._source.caption,
    };
    let aliasObj = item._source.names.map((alias) => {
      return {
        open_sanction_id: item._id,
        name: item._source.caption,
        alias: alias,
      };
    });
    return [actualNameObj, ...aliasObj];
  });
  let finalList = [];
  jsonResponse.forEach((list) => {
    finalList = [...finalList, ...list];
  });
  // convert JSON array to CSV string
  converter.json2csv(finalList, (err, csv) => {
    if (err) {
      throw err;
    }

    // write CSV to a file
    fs.writeFileSync("open_sanction_mini.csv", csv);
  });
}

